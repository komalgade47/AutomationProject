public class Lab005_Literals {
    public static void main ( String[] arg )
    {
//escape characters
        char new_line = '\n';
        char tab_line ='\t';
        char back_space ='\b';
        char carriage_return='\r';

        System.out.println("Komal" + new_line + "Gade");
        System.out.println("Komal" + tab_line + "Gade");
        System.out.println("Komal" + back_space + "Gade");
        System.out.println("Komal" + carriage_return + "Gade");
    }
}
